#!/usr/bin/env python3
"""
    Basic annotations - to_str
"""


def to_str(n: float) -> str:
    """
    Converts a floating-point number to a string and returns it.

    Args:
        n (float): The floating-point number to be converted to a string.

    Returns:
        str: The string representation of 'n'.
    """
    return str(n)
